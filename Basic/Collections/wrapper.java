package Basic.Collections;
import java.lang.*;
import java.util.*;

// Wrapper class is just object type of primitive data type like int is Integer and float is Float 
// and so on, this class is helpful as it enables us to apply methods on objects created using these classes

public class wrapper {
    public static void main(String[] args) {
        // 
        Integer i =10;
        System.out.println(i);
        System.out.println(i.intValue());
        
    }
}
